#include <stdio.h>

void	ft_swap(int *a, int *b);

int		main(void)
{
	int a;
	int b;

	a = 5;
	b = 3;
	printf("before:\na = %d\nb = %d\n", a, b);
	ft_swap(&a, &b);
	printf("after:\na = %d\nb = %d\n", a, b);
}
