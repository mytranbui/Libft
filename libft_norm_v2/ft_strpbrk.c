#include "libft.h"

char	*ft_strpbrk(const char *s1, const char *s2)
{
	if (!s1 || !s2)
		return (NULL);
	while (*s1)
	{
		if (ft_strchr(s2, *s1))
			return (char *)s1;
		s1++;
	}
	return (NULL);
}
